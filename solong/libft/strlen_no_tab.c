#include "libft.h"

size_t	strlen_no_tab(const char *s)
{
	size_t	len;

	len = 0;
	if (!s)
		return (0);
	while (*s != '\0' && *s != '\n')
	{
		++len;
		s = s + 1;
	}
	return (len);
}